def calculator():
  print(logo)
  fno = input("What is the first number?: ")
  res = fno
  print('+','-','*','/',sep='\n')
  while(True):
    op = input("Pick an operation: ")
    next = input("What's the next number?: ")
    res = str(eval(res+op+next))
    choice = input(f"Type y to continue calculating with {res}, type 'n' to start a new calculation, or type 'q' to quit altogether: ")
    if(choice=='y'):
        continue
    elif(choice=='n'):
        os.system('cls')
        calculator()
    elif(choice=='q'):
      print("Exiting the calculator....")
      sys.exit(0)

calculator()